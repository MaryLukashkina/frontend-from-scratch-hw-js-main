let str = "hello world from javascript"

function capitalizeWords(string) {
  let res = ''
  let capitalizeNext = true;

  for (let i = 0; i < string.length; i++) {
    const currentChar = string[i];

    if (currentChar === ' ') {
      res = res + currentChar // –î–æ–±–∞–≤–ª—è–µ–º –ø—Ä–æ–±–µ–ª –≤ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
      capitalizeNext = true; // –°–ª–µ–¥—É—é—â–∏–π —Å–∏–º–≤–æ–ª –±—É–¥–µ—Ç –ø–µ—Ä–≤—ã–º –≤ –Ω–æ–≤–æ–º —Å–ª–æ–≤–µ
    } else {
      if (capitalizeNext) {
        res = res + currentChar.toUpperCase();
        capitalizeNext = false;// –ü–æ—Å–ª–µ –ø–µ—Ä–≤–æ–≥–æ —Å–∏–º–≤–æ–ª–∞ —Å–ª–µ–¥—É—é—â–∏–º–∏ –Ω–µ –Ω—É–∂–Ω–æ
      } else {
        res = res + currentChar  // –î–æ–±–∞–≤–ª—è–µ–º —Å–∏–º–≤–æ–ª –∫–∞–∫ –µ—Å—Ç—å
      }
    }

  }
  return res;
}

